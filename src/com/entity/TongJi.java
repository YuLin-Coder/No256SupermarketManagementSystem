package com.entity;

public class TongJi {
	private String amount;
	private String name;
	private String sum;
	private String liyun;

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSum() {
		return sum;
	}

	public void setSum(String sum) {
		this.sum = sum;
	}

	public String getLiyun() {
		return liyun;
	}

	public void setLiyun(String liyun) {
		this.liyun = liyun;
	}
	
	

}
